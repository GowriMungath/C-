//datatypes
#include<iostream>
using namespace std;
int main()
{
    int a=5;
    float f=8.9;
    char ch='c';
    bool b=0;
    cout<<a<<" is an Integer with size "<<sizeof(int)<<" bytes. "<<endl;
    cout<<f<<" is a floating point value with size "<<sizeof(float)<<" bytes. "<<endl;
    cout<<ch<<" is a character with size "<<sizeof(char)<<" byte. "<<endl;
    cout<<b<<" is a boolean value with size "<<sizeof(bool)<<" byte. "<<endl;
}
